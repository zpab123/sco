// 远端->本地握手请求
type HandshakeReq struct {
	Key string `json:"key"` // 通信key
}

// 本地->远端握手回复
type HandshakeRes struct {
	Code      uint32 `json:"code"`      // 握手结果
	Heartbeat uint32 `json:"heartbeat"` // 心跳时间
}
